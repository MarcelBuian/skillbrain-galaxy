const a = 0;
const b = 5;

// In Javascript, impartirea se face in numere complexe, prin urmare inifinitul are valoare.
const c = b/a;
const d = -b/a;

console.log(c);

// Outputul arata mai frumos cand avem un text ajutator
console.log(b + "/" + a + "=" + c);

// Folosind accent grav output-ul este mai frumos:
console.log(`${b}/${a}=${c}`);


// Folosind accent grav output-ul este mai frumos:
console.log(`${-b}/${a}=${d}`);



